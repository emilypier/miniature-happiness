// Write code to print all numbers from 1 to `num`
// Assume `num` will be a positive number

var logNums = function(num) {
  //loop through numbers 1 to num
  // starting at 1, until num is less than or equal to 1, increment through numbers.
  for (var i = 1; i <= num; i++) {
    console.log(i);
  };
};
